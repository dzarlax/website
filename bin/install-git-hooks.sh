#!/usr/bin/env bash
# Install repo-local git hooks.
#
# Currently installs:
#   - pre-commit → runs bin/lint-frontmatter.sh against staged article files
#
# Idempotent: re-running just refreshes the hook. To uninstall, delete
# .git/hooks/pre-commit manually (or run `git config --unset core.hooksPath`
# if you'd rather use core.hooksPath later).
#
# Usage:
#   bin/install-git-hooks.sh

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"

GIT_DIR="$(git -C "$ROOT" rev-parse --git-dir)"
HOOKS_DIR="$GIT_DIR/hooks"
mkdir -p "$HOOKS_DIR"

HOOK="$HOOKS_DIR/pre-commit"
cat > "$HOOK" <<'EOF'
#!/usr/bin/env bash
# Auto-generated by bin/install-git-hooks.sh — do not edit by hand.
# Lints staged Hugo article frontmatter before allowing the commit.

set -euo pipefail

ROOT="$(git rev-parse --show-toplevel)"

# Collect staged article files (Added, Copied, Modified, Renamed) under
# hugo/content/articles/.
mapfile -t STAGED < <(
  git diff --cached --name-only --diff-filter=ACMR \
    -- 'hugo/content/articles/*.md' 'hugo/content/articles/**/*.md' 2>/dev/null \
    | grep -v '/_index\.md$' || true
)

if [ "${#STAGED[@]}" -eq 0 ]; then
  exit 0
fi

# Resolve to absolute paths so the linter works regardless of cwd.
declare -a ABS=()
for f in "${STAGED[@]}"; do
  ABS+=("$ROOT/$f")
done

exec "$ROOT/bin/lint-frontmatter.sh" "${ABS[@]}"
EOF

chmod +x "$HOOK"
chmod +x "$ROOT/bin/lint-frontmatter.sh" 2>/dev/null || true

echo "✓ Installed pre-commit hook → $HOOK"
echo "  Hook lints staged hugo/content/articles/**/*.md via bin/lint-frontmatter.sh"
echo "  Bypass (only when you mean it): git commit --no-verify"
